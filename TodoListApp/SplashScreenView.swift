import SwiftUI
import SwiftData

struct SplashScreenView: View {
    @State private var isActive = false
    @State private var size = 0.8
    @State private var opacity = 0.5
    
    var body: some View {
        NavigationStack {
            if isActive {
                AuthenticationView()
            } else {
                VStack {
                    VStack {
                        Text("TodoList")
                            .font(.system(size: 48))
                            .fontWeight(.bold)
                            .foregroundColor(.blue)
                    }
                    .scaleEffect(size)
                    .opacity(opacity)
                    .onAppear {
                        withAnimation(.easeIn(duration: 1.2)) {
                            self.size = 0.9
                            self.opacity = 1.0
                        }
                    }
                }
                .onAppear {
                    DispatchQueue.main.asyncAfter(deadline: .now() + 3.0) {
                        withAnimation {
                            self.isActive = true
                        }
                    }
                }
            }
        }
    }
} 